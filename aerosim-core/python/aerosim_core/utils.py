def feet_to_meters(feet) -> float:
    return feet * 0.3048


def meters_to_feet(meters) -> float:
    return meters / 0.3048


def feet_per_sec_to_knots(feet_per_sec) -> float:
    return feet_per_sec * 0.592484


def knots_to_feet_per_sec(knots) -> float:
    return knots / 0.592484


def meters_per_sec_to_knots(meters_per_sec) -> float:
    return meters_per_sec * 1.94384


def knots_to_meters_per_sec(knots) -> float:
    return knots / 1.94384
